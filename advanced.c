#include "ft_shell.h"

void			ft_home_end(char ch, t_inp *inp)
{
	if (ch == 11)
	{
		while (inp->ind > 0)
		{
			write(1, "\e[D", 3);
			inp->ind--;
		}
	}
	if (ch == 12)
	{
		while (inp->arg[inp->ind])
		{
			write(1, "\e[C", 3);
			inp->ind++;
		}
	}
}
